class Volume extends Src {
    String volume (String bore, String stroke) {
        konvert(bore, stroke);
        Volum = (float) (Math.PI * ((A/2)*(A/2))*B);
        invert( Volum);
        return out;
    }
    
    String tVol (String sil){
        konvert(sil,"0");
        tVol= Volum * A;
        invert (tVol);
        return out;
    }
    
}
